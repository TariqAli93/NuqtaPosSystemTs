// ============================================================
// Business Preset Definitions for Seed Script
// All prices in IQD (Iraqi Dinar) — thousands-based, no decimals
// ============================================================

export type PresetKey =
  | 'supermarket'
  | 'phones'
  | 'clothing'
  | 'pharmacy'
  | 'electronics'
  | 'restaurant';

export interface PresetCategory {
  name: string;
  description: string;
}

export interface PresetProduct {
  name: string;
  sku: string;
  categoryRef: string; // matches a category name from the same preset
  costPrice: number;
  sellingPrice: number;
  stock: number;
  minStock: number;
  unit: string;
  supplier: string;
  status: 'available' | 'out_of_stock' | 'discontinued';
}

export interface PresetCustomer {
  name: string;
  phone: string;
  address: string;
  city: string;
  notes: string;
}

export interface PresetSale {
  customerRef: number; // index into customers array
  discount: number;
  paymentType: 'cash' | 'installment' | 'mixed';
  paidAmount: number;
  notes: string;
  items: { productRef: string; quantity: number }[]; // productRef = SKU
  interestRate?: number;
  installmentCount?: number;
}

export interface Preset {
  label: string;
  categories: PresetCategory[];
  products: PresetProduct[];
  customers: PresetCustomer[];
  sales: PresetSale[];
}

